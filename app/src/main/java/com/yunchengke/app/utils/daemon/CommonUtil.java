    /***
     * 半角转换为全角
     *
     * @param input
     * @return
     */
    public static String ToSBC(String input) {
        if (!TextUtils.isEmpty(input)) {
            char c[] = input.toCharArray();
            for (int i = 0; i < c.length; i++) {
                if (c[i] == ' ') {
                    c[i] = '\u3000';
                } else if (c[i] < '\177') {
                    c[i] = (char) (c[i] + 65248);
                }
            }
            return new String(c);
        }
        return "";
    }

